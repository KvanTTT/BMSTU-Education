namespace TravellingSalesmanProblem
{
    class City
    {
        int X, Y;

        public City(int X, int Y)
        {
            this.X = X;
            this.Y = Y;
        }

        public int GetX()
        {
            return X;
        }

        public int GetY()
        {
            return Y;
        }
    }
}
